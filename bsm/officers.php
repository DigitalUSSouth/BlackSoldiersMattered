<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman";
color:#262626'>In the long list of apprehensions white Americans expressed
concerning African Americans’ service in World War I, the question of training
and commissioning black officers proved a major source of political tension. The
eventual compromise led to one class of officers trained far from the South, in
Iowa. From the segregated officer training camp at Ft. Des Moines, 1,000 newly
volunteered civilians and 250 experienced soldiers trained from June to October,
and 639 African Americans earned their commission on October 15, 1917.<a
href="#_edn1" name="_ednref1" title=""><span class=MsoEndnoteReference><span
class=MsoEndnoteReference><span style='font-size:12.0pt;line-height:107%;
font-family:"Times New Roman";color:#262626'>[i]</span></span></span></a></span></p>

<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman";
color:#262626'>Consistent with the national success rates at Ft. Des Moines, at
least twenty-seven black North Carolinians became officers in October, just
over half of the forty-nine men identified for officer training. Fourteen North
Carolinians were commissioned 2<sup>nd</sup> lieutenants, the lowest rank;
eight as 1<sup>st</sup> lieutenants; and five as captains, the highest rank for
black soldiers. Sixteen of twenty-seven men commissioned entered Ft. Des Moines
from civilian life, and the other eleven underwent officer training as
experienced soldiers. Secretary Baker assigned these officers to infantry,
artillery, and engineering units of the newly created 92<sup>nd</sup> Division,
and he assured military and civilian leaders that all commissioned black
officers would be in the non-southern camps of the segregated division.<a
href="#_edn2" name="_ednref2" title=""><span class=MsoEndnoteReference><span
class=MsoEndnoteReference><span style='font-size:12.0pt;line-height:107%;
font-family:"Times New Roman";color:#262626'>[ii]</span></span></span></a></span></p>

<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman";
color:#262626'>These officers were among North Carolina’s most distinguished
African American soldiers, and many became accomplished men after the war. Commissioned
officers from civilian life included <b>2<sup>nd</sup> Lt. Robert Smalls
Bampfield of Wilmington</b>, the grandson of famed Civil War hero and
Reconstruction era politician from Beaufort, South Carolina, Robert Smalls. Educated
at Johnson C. Smith, Bampfield also served in World War II, lived much of his
civilian life in Washington, D.C., and is buried in Arlington National
Cemetery. Another Johnson C. Smith alumnus <b>2<sup>nd</sup> Lt. Gurney E.
Nelson of Greensboro,</b> became a career educator after pursuing graduate
education at the University of Chicago and Ohio State. <b>2<sup>nd</sup> Lt.
Lovelace B. Capehart, Jr. of Raleigh</b> became a pharmacist and physician
after the war. <b>2<sup>nd</sup> Lt. Russell C. Atkins of Winston-Salem</b> was
educated at Hampton Institute in Virginia. Atkins worked before and after the
war in agricultural education at Tuskegee. His father had been president of
State Teachers College in Winston Salem. <b>Lt. Bravid W. Harris, Jr. of
Warrenton</b> became an Episcopal priest and bishop and eventually a missionary
to Liberia in the ensuing decades. Most black North Carolina officers led men
of the 365<sup>th</sup> Infantry—the regiment with which most black North
Carolina infantrymen served. </span><span style='font-size:12.0pt;line-height:
200%;font-family:"Times New Roman"'>Unfortunately for the state, many of these
talented and educated native sons who returned from the war seeking
opportunities found them outside of North Carolina.<a href="#_edn3"
name="_ednref3" title=""><span class=MsoEndnoteReference><span
style='color:#262626'><span class=MsoEndnoteReference><span style='font-size:
12.0pt;line-height:107%;font-family:"Times New Roman";color:#262626'>[iii]</span></span></span></span></a></span></p>

<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman";
color:#262626'>Those commissioned officers with prior military experience
included, among others, <b>captains Moody Staten of Lincoln, Herbert Avery of
Morganton, and Samuel Reid of Concord and lieutenants James W. Alston of
Raleigh and Thomas Bullock of Henderson.</b> Except for Capt. Staten, these men
were in their forties at the time of commissioning. Their ages and military
experiences set them apart from the other officers and nearly all the enlisted
men. Lt. Alston, a veteran of the Spanish-American War, had an atypical
experience for North Carolina black soldiers in other ways. He was one of the
few North Carolinians who served as an officer in the 93<sup>rd</sup> Division,
rather than the 92<sup>nd</sup>, leading men in the 372<sup>nd</sup> Infantry
Regiment. Lt. Alston also corresponded during the war with a white employer,
Herbert H. Brimley, curator of the State Museum, sharing his experiences and
perspective. In his correspondence, Lt. Alston continually expressed optimism
and conveyed his patriotic, anti-German determination to defeat the enemy. “Fritz
can fight like the very devil when he is under cover and has the most men, but
can’t stand the <u>Yankee steel</u> and these Yanks, white &amp; black sure
love to use the bayonet whenever they can get near enough to him.”<a
href="#_edn4" name="_ednref4" title=""><span class=MsoEndnoteReference><span
class=MsoEndnoteReference><span style='font-size:12.0pt;line-height:107%;
font-family:"Times New Roman";color:#262626'>[iv]</span></span></span></a> </span></p>

<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman";
color:#262626'>Another of the seasoned soldiers was </span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman"'>Lt.
Thomas Bullock, who led men in the 367<sup>th </sup>Infantry. With far more
military experience than most North Carolinians, Lt. Bullock also had been a
veteran of the Spanish-American War and had served three years in the
Philippines with the 25<sup>th</sup> Infantry, one of the U.S. Army’s four
all-black regular army units. In addition to his military experience, Lt.
Bullock was college educated. A graduate of Lincoln University, the
Pennsylvania alma mater of Thurgood Marshall, Lt. Bullock also taught at
Williston Industrial School in Wilmington. In 1916 when the 15<sup>th</sup>
Infantry Regiment of the New York National Guard organized, Bullock was among
the first to volunteer. William Hayward, commanding officer of the New York
regiment, readily identified him as an ideal candidate for officer training. So
Bullock left the regiment that later became the 369<sup>th</sup> “Harlem
Fighters” to lead men in the 92<sup>nd</sup> Division.<a href="#_edn5"
name="_ednref5" title=""><span class=MsoEndnoteReference><span
class=MsoEndnoteReference><span style='font-size:12.0pt;line-height:107%;
font-family:"Times New Roman"'>[v]</span></span></span></a></span></p>

<p class=MsoNormal style='text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman"'>While
Ft. Des Moines only commissioned one class of African American officers, the
army had a few other opportunities for black soldiers to become commissioned
officers. African Americans from North Carolina also became commissioned as
physicians. Similar to most of their national counterparts, medical officers
from North Carolina, who treated the wounded in combat, were recent medical
school graduates. Two of those men, <b>Lt. Egbert Theophilus Scott of
Wilmington and Lt. Max Constuart King of Franklinton, attended Leonard Medical
College at Shaw University</b>. King finished his studies and graduated from
Meharry Medical School in Nashville. Although born and educated in North
Carolina at Biddle University (name changed to Johnson C. Smith in 1923) and
Shaw University, Scott left his home state after the war for opportunities in
Pennsylvania where he practiced medicine for fifty years. North Carolinians
were also among the commissioned black officers in the chaplaincy. For example <b>Lt.
Alexander Huntington Hatwood, pastor of the Grace A.M.E. Church of Salisbury,
served as a Methodist chaplain</b> at Camp Taylor in Louisville, Kentucky.<span
class=MsoEndnoteReference> <a href="#_edn6" name="_ednref6" title=""><span
class=MsoEndnoteReference><span style='font-size:12.0pt;line-height:107%;
font-family:"Times New Roman"'>[vi]</span></span></a></span></span></p>

